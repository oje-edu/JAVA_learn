package theorie;

public class _210_MathematischeFunktionen {
    
    public static void main(String[] args) {
	
	// Power - Potenzieren
	System.out.println(Math.pow(2, 8));  // 256
	
	// Betrag - absolute value
	System.out.println(Math.abs(-7));  // 7
	
	// Quadratwurzel
	// double Math.sqrt(int zahl)
	System.out.println(Math.sqrt(64));    // 8.0
	// double Math.sqrt(double zahl)
	System.out.println(Math.sqrt(64.8));  // 8.049844718999243
	
    }

}
